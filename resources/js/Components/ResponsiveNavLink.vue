        : 'block w-full ps-3 pe-4 py-2 border-l-4 border-transparent text-start text-base font-medium text-gray-600 hover:text-tPrimaryColor hover:bg-primaryBGColor hover:border-gray-300 focus:outline-none focus:text-tPrimaryColor focus:bg-primaryBGColor focus:border-gray-300 transition duration-150 ease-in-out'
);
</script>

<template>
    <Link :href="href" :class="classes">
        <slot />
    </Link>
</template>
